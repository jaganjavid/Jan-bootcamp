

// Primitive value

// String
const name = "Jagan";

console.log(name);
console.log(typeof name);

// Number 
const age = 27;

console.log(age);
console.log(typeof age);

// Boolean 
const hadDinner = false;

console.log(hadDinner);
console.log(typeof hadDinner);

// Null
const box = null;

console.log(box);
console.log(typeof box);

// undefined
let undef;

console.log(undef);
console.log(typeof undef);

// Reference Value

// Array
const myArr = ["Jagan", "Javid", 27, true, null, undefined];

console.log(myArr);
console.log(typeof myArr);

// Object (Key : Value) && (.)

const car = {
    // Property
    name:"BMW",
    color:"Black",
    model:"5 series",
    year:2025
}

console.log(car);
console.log(typeof car);